std::vector<char> keyBuffer;

Keys::Keys()
{
	for (int a=0;a<256;++a)
	{
		keys[a]=false;
		oldKeys[a]=false;
		keysRepeat[a]=false;
	}
}

Keys::~Keys()
{

}

void Keys::setOldKeys()
{
	for (int a=0;a<256;++a)
	{
		oldKeys[a]=keys[a];
		keysRepeat[a]=false;
	}
}
